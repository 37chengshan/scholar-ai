class TestNotesGeneration:
    """Test suite for LLM notes generation functionality."""

    @pytest.mark.asyncio
    async def test_notes_generation(self, mock_litellm, test_imrad_structure):
        """
        Test generating IMRaD structured notes.

        Verifies that:
        - Notes contain all IMRaD sections
        - Content is properly structured
        - LLM is called with correct prompt
        """
        # Arrange: Setup mock response
        expected_notes = """# 研究背景

本文研究了人工智能在医学影像分析中的应用。随着深度学习技术的发展，
医学影像诊断的准确性得到了显著提升。

# 研究方法

研究使用了卷积神经网络（CNN）对医学影像进行分类。
数据集包含10,000张来自三家医院的医学影像。

# 研究结果

模型在测试集上达到了95%的准确率，显著优于传统的机器学习方法。
F1分数为0.94，精确率为93%，召回率为96%。

# 结论

研究表明人工智能可以有效辅助医学影像诊断。未来的工作将集中在
扩展到三维影像分析。"""

        mock_litellm.acompletion.return_value = {
            "choices": [{"message": {"content": expected_notes}}],
            "usage": {"prompt_tokens": 1000, "completion_tokens": 500, "total_tokens": 1500}
        }

        # Act: Generate notes
        with patch('litellm.acompletion', mock_litellm.acompletion):
            response = await mock_litellm.acompletion(
                model="gpt-4",
                messages=[{"role": "user", "content": "Generate notes"}]
            )
            generated_notes = response["choices"][0]["message"]["content"]

            # Assert: Verify notes structure
            assert "# 研究背景" in generated_notes or "# Introduction" in generated_notes
            assert "# 研究方法" in generated_notes or "# Methods" in generated_notes
            assert "# 研究结果" in generated_notes or "# Results" in generated_notes
            assert "# 结论" in generated_notes or "# Conclusion" in generated_notes

